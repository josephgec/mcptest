# github-comment
# ---------------------------------------------------------------------------


def _resolve_pr_number(event_path: str | None) -> int | None:
    if not event_path:
        return None
    p = Path(event_path)
    if not p.exists():
        return None
    try:
        data = json.loads(p.read_text(encoding="utf-8"))
    except json.JSONDecodeError:
        return None
    # Pull request events have `pull_request.number`; issue_comment events
    # have `issue.number` (and `issue.pull_request` present).
    if "pull_request" in data and isinstance(data["pull_request"], dict):
        return int(data["pull_request"].get("number", 0)) or None
    if "issue" in data and isinstance(data["issue"], dict):
        return int(data["issue"].get("number", 0)) or None
    if "number" in data:
        return int(data.get("number", 0)) or None
    return None


def post_comment(
    repo: str,
    pr_number: int,
    body: str,
    token: str,
    *,
    opener: Any = None,
) -> int:
    """POST a PR comment. Returns the HTTP status code.

    `opener` is injectable so tests can drive this without real network.
    Resolved at call time (not at function-definition time) so tests can
    monkeypatch `urllib.request.urlopen` via the module attribute.
    """
    if opener is None:
        opener = _urllib_request.urlopen
    url = f"https://api.github.com/repos/{repo}/issues/{pr_number}/comments"
    payload = json.dumps({"body": body}).encode("utf-8")
    req = _urllib_request.Request(
        url,
        data=payload,
        headers={
            "Authorization": f"Bearer {token}",
            "Accept": "application/vnd.github+json",
            "Content-Type": "application/json",
            "X-GitHub-Api-Version": "2022-11-28",
            "User-Agent": "mcptest-action",
        },
        method="POST",
    )
    try:
        response = opener(req)
    except HTTPError as exc:
        return exc.code
    except URLError as exc:  # pragma: no cover - network flake
        raise click.ClickException(f"failed to POST comment: {exc}")
    status = getattr(response, "status", None) or response.getcode()
    return int(status)


@click.command(
    help=(
        "Post a PR comment from mcptest JSON output. "
        "Reads GITHUB_TOKEN, GITHUB_REPOSITORY, and GITHUB_EVENT_PATH from env."
    )
)
@click.option(
    "--input",
    "input_path",
    default="-",
    type=click.Path(dir_okay=False),
    help="Path to `mcptest run --json` output. Use '-' (default) for stdin.",
)
@click.option(
    "--from-env",
    is_flag=True,
    help="Read token / repo / PR from the standard GitHub Actions env.",
)
@click.option("--repo", default=None, help="Override GITHUB_REPOSITORY.")
@click.option("--pr", "pr_number", type=int, default=None, help="Override PR number.")
@click.option("--token", default=None, help="Override GITHUB_TOKEN.")
@click.option(
    "--dry-run",
    is_flag=True,
    help="Print the comment body to stdout instead of posting.",
)
def github_comment_command(
    input_path: str,
    from_env: bool,
    repo: str | None,
    pr_number: int | None,
    token: str | None,
    dry_run: bool,
) -> None:
    payload = _load_payload(input_path)
    body = format_comment(payload)

    if dry_run:
        click.echo(body)
        return

    resolved_repo = repo or os.environ.get("GITHUB_REPOSITORY")
    resolved_token = token or os.environ.get("GITHUB_TOKEN")
    resolved_pr = pr_number
    if resolved_pr is None:
        resolved_pr = _resolve_pr_number(os.environ.get("GITHUB_EVENT_PATH"))

    if not resolved_repo or not resolved_token or not resolved_pr:
        raise click.ClickException(
            "missing GitHub context — need --repo/--pr/--token or GITHUB_REPOSITORY "
            "+ GITHUB_TOKEN + GITHUB_EVENT_PATH from the runner"
        )

    status = post_comment(
        resolved_repo, int(resolved_pr), body, resolved_token
    )
    if 200 <= status < 300:
        click.echo(f"posted comment (HTTP {status})")
    else:
        raise click.ClickException(
            f"GitHub API returned HTTP {status} when posting comment"
        )


