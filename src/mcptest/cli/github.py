"""GitHub integration commands: `github-comment` and `badge`.

Both commands are intentionally stdlib-only (no httpx) so the action can run
in a minimal Python environment without extra dependencies.

`github-comment` builds a Markdown summary from a `mcptest run --json` payload
and POSTs it to the pull request via the GitHub REST API. Environment:

    GITHUB_TOKEN       — bearer token for api.github.com
    GITHUB_REPOSITORY  — "owner/repo"
    GITHUB_EVENT_PATH  — path to the webhook payload (parsed for PR number)

`badge` writes a shields.io-compatible endpoint JSON based on the same run
JSON, suitable for `https://img.shields.io/endpoint?url=...`.
"""

from __future__ import annotations

import json
import os
import sys
from pathlib import Path
from typing import Any
from urllib import request as _urllib_request
from urllib.error import HTTPError, URLError

import click


# ---------------------------------------------------------------------------
# Shared helpers
# ---------------------------------------------------------------------------


def _load_payload(input_path: str | None) -> dict[str, Any]:
    if input_path and input_path != "-":
        data = Path(input_path).read_text(encoding="utf-8")
    else:
        data = sys.stdin.read()
    if not data.strip():
        raise click.ClickException("expected JSON on stdin or via --input")
    try:
        return json.loads(data)
    except json.JSONDecodeError as exc:
        raise click.ClickException(f"invalid JSON: {exc}") from exc


def format_comment(payload: dict[str, Any]) -> str:
    """Build a Markdown PR comment from `mcptest run --json` output.

    Optionally includes:
    - A **Metrics** section when ``metric_summary`` is present in the payload.
    - A **Metric Regressions** section when a ``comparison`` key (from
      ``mcptest compare --json``) is present and contains regressions.
    """
    passed = payload.get("passed", 0)
    failed = payload.get("failed", 0)
    total = payload.get("total", passed + failed)
    emoji = "✅" if failed == 0 else "❌"

    lines = [
        f"{emoji} **mcptest**: {passed} passed, {failed} failed ({total} total)",
        "",
    ]

    failing = [c for c in payload.get("cases", []) if not c.get("passed", False)]
    if failing:
        lines.append("### Failures")
        for case in failing:
            suite = case.get("suite", "?")
            name = case.get("case", "?")
            err = case.get("error")
            lines.append(f"- **{suite}::{name}**")
            if err:
                lines.append(f"  - error: {err}")
            for a in case.get("assertions", []):
                if not a.get("passed", False):
                    lines.append(f"  - ✗ {a.get('name')}: {a.get('message')}")
        lines.append("")

    # Metrics summary section.
    metric_summary: dict[str, float] = payload.get("metric_summary", {})
    if metric_summary:
        lines.append("### Metrics")
        for name in sorted(metric_summary):
            score = metric_summary[name]
            indicator = "🟢" if score >= 0.8 else "🟡" if score >= 0.5 else "🔴"
            lines.append(f"- {indicator} **{name}**: {score:.3f}")
        lines.append("")

    # Metric regression section (populated when compare --json output is merged in).
    comparison: dict[str, Any] = payload.get("comparison", {})
    if comparison:
        regressions = [
            d for d in comparison.get("deltas", []) if d.get("regressed", False)
        ]
        if regressions:
            lines.append("### Metric Regressions")
            for r in regressions:
                name = r.get("name", "?")
                b = r.get("base_score", 0.0)
                h = r.get("head_score", 0.0)
                diff = r.get("delta", h - b)
                lines.append(
                    f"- ⚠️ **{name}**: {b:.3f} → {h:.3f} ({diff:+.3f})"
                )
            lines.append("")

    lines.append("<sub>Generated by <a href='https://github.com/josephgec/mcptest'>mcptest</a>.</sub>")
    return "\n".join(lines)


