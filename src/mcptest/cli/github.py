def build_badge(payload: dict[str, Any]) -> dict[str, Any]:
    """Build a shields.io endpoint badge payload."""
    passed = payload.get("passed", 0)
    failed = payload.get("failed", 0)
    total = payload.get("total", passed + failed)
    color = "brightgreen" if failed == 0 and total > 0 else (
        "red" if failed > 0 else "lightgrey"
    )
    message = (
        "no tests"
        if total == 0
        else f"{passed}/{total} passing"
        if failed > 0
        else f"{total} passing"
    )
    return {
        "schemaVersion": 1,
        "label": "mcptest",
        "message": message,
        "color": color,
    }


@click.command(help="Emit a shields.io endpoint badge from mcptest JSON output.")
@click.option(
    "--input",
    "input_path",
    default="-",
    type=click.Path(dir_okay=False),
    help="Path to `mcptest run --json` output. Use '-' (default) for stdin.",
)
@click.option(
    "--output",
    "output_path",
    default=None,
    type=click.Path(dir_okay=False),
    help="Where to write the badge JSON (default: stdout).",
)
def badge_command(input_path: str, output_path: str | None) -> None:
    payload = _load_payload(input_path)
    badge = build_badge(payload)
    text = json.dumps(badge, indent=2)
    if output_path:
        Path(output_path).write_text(text + "\n", encoding="utf-8")
    else:
        click.echo(text)
